package com.freshdirect.smartstore.dsl;

public class ClassCompileException extends CompileException {

    public ClassCompileException() {
    }

    public ClassCompileException(String message) {
        super(message);
    }

    public ClassCompileException(Throwable cause) {
        super(cause);
    }

    public ClassCompileException(String message, Throwable cause) {
        super(message, cause);
    }

}
